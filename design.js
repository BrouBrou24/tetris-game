let board = [ ];
const cvs = document.getElementById("tetris");
const ctx = cvs.getContext("2d");

for ( let r = 0; r < 20; r++){
      board[r] = [ ];
      for( let c = 0; c < 10; c++){
        board[r][c] = EMPTY;
    }
}

function drawSquare(x, y, color) {
    ctx.fillStyle = color;
    ctx.fillRect(x, y, SQ, SQ);
    ctx.strokeStyle = "black";
    ctx.strokeRect(x, y, SQ, SQ);
}

